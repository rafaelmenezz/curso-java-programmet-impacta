package curso.java.veolia.booleanos;

public class VariaveisBooleanas {

	public static void main(String[] args) {
		
		boolean b1 = true;
		boolean b2 = false;
		
		int n = 100;
		
		boolean b3 = n > 0;
		boolean b4 = !b3;
		
		System.out.println("b1: "+ b1);
		System.out.println("b2: "+ b2);
		System.out.println("b3: "+ b3);
		System.out.println("b4: "+ b4);
	}
}
